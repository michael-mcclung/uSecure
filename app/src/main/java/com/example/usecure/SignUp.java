// packages
package com.example.usecure;

// sign up class
public class SignUp {

    // variables
    private String email, password, first, last, add, number;
    
    // function used to get sign up credentials
    public SignUp(String email, String password, String first, String last, String add, String number){
        this.add = add;
        this.email = email;
        this.first = first;
        this.last = last;
        this.number = number;
        this.password = password;
    }
}
